    private void autoNumber() throws SQLException
    {
        String noNota = "MST000";
        int i = 0;
        try{
            Statement st = conn.createStatement();
            String sql = "select kd_mst from tb_mustahiq";
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                noNota = rs.getString("kd_mst");
            }
            noNota = noNota.substring(4);
            i = Integer.parseInt(noNota)+1;
            noNota = "00" +i;
            noNota = "MST" + noNota.substring(noNota.length()-3);
            txt_kode.setText(noNota);
        }catch (SQLException e){
            System.out.println(e.getMessage());
        }
    }
