    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        String sql = "insert into tb_mustahiq values (?,?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1,txt_kode.getText());
            stat.setString(2,txt_nama.getText());
            stat.setString(3,txt_usia.getText());
            stat.setString(4,cb_golongan.getSelectedItem().toString());
            stat.setString(5,txt_alamat.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data Berhasil di simpan");
            kosong();
            txt_nama.requestFocus();
            datatable();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Data Tidak di simpan"+e);
        }
        try {
            autoNumber();
        } catch (SQLException ex) {
            Logger.getLogger(data_mustahik.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btn_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ubahActionPerformed
        try{
            String sql ="update tb_mustahiq set nama=?,usia=?,golongan=?,alamat=? where kd_mst=?";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1,txt_nama.getText());
            stat.setString(2,txt_usia.getText());
            stat.setString(3,cb_golongan.getSelectedItem().toString());
            stat.setString(4,txt_alamat.getText());
            stat.setString(5,txt_kode.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data Berhasil di Ubah");
            kosong();
            txt_kode.requestFocus();
            datatable();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Data Gagal Di Ubah"+e);
        }
    }//GEN-LAST:event_btn_ubahActionPerformed

