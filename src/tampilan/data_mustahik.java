    protected void datatable(){
        Object [] data = {"Kode Mustahik","Nama","Usia","Golongan","Alamat"};
        tabmode = new DefaultTableModel(null,data);
        jTable1.setModel(tabmode);
        String sql = "select * from tb_mustahiq";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet dataa = stat.executeQuery(sql);
            while(dataa.next()){
                String a = dataa.getString("kd_mst");
                String b = dataa.getString("nama");
                String c = dataa.getString("usia");
                String d = dataa.getString("golongan");
                String e = dataa.getString("alamat");
                
                String[] getData = {a,b,c,d,e,};
                tabmode.addRow(getData);
                }
            } catch (SQLException ex){
                    JOptionPane.showMessageDialog(null,"Error di"+ex);
            }
        }
